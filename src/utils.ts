export function createCalendarMessage(googleCalendarEvent: Schema$Event): string {
  const { summary = '', description = '' } = googleCalendarEvent
  let message = ''
  if (summary) {
    message += summary
  }
  if (description) {
    message += `\n${description}`
  }
  return message
}

